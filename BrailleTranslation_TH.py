def mirror(a):
    """Mirror cell row: สลับจุดซ้าย↔ขวา
    0->0, 1->2, 2->1, 3->3"""
    if a == 1:
        return 2
    if a == 2:
        return 1
    return a

def star(a):
    """แปลงค่า cell row เป็น ASCII art
    3 = ทั้งสองจุด, 2 = จุดขวา, 1 = จุดซ้าย, 0 = ว่าง"""
    if a == 3:
        return "**| "
    if a == 2:
        return "*_| "
    if a == 1:
        return "_*| "
    return "__| "


def translate(text):
    """แปลงข้อความภาษาไทยเป็นอักษรเบรลล์ (ASCII art 3 แถว)"""
    lines = ["", "", ""]

    for ch in text:
        if ch == ' ':
            # ช่องว่าง
            lines[0] += star(0)[::-1]
            lines[1] += star(0)[::-1]
            lines[2] += star(0)[::-1]
        elif ch in THAI_BRAILLE_MAP:
            dot_list = THAI_BRAILLE_MAP[ch]
            for dots in dot_list:
                cell = dots_to_cell(dots)
                lines[0] += star(cell[0])[::-1]
                lines[1] += star(cell[1])[::-1]
                lines[2] += star(cell[2])[::-1]
                # lines[0] += star(cell[0])
                # lines[1] += star(cell[1])
                # lines[2] += star(cell[2])
        else:
            # ตัวอักษรที่ไม่รู้จัก -> แสดงเป็นช่องว่าง
            lines[0] += star(0)[::-1]
            lines[1] += star(0)[::-1]
            lines[2] += star(0)[::-1]

    return lines


